public class MainTrunchieresiRotunjire {
    public static void main(String[] args) {
        // Exemplu de numere

        double numar1 = 5.3;
        double numar2 = 5.48;
        double numar3 = -3.4;

        System.out.println("Numărul: " + numar1);
        System.out.println("Math.floor(): " + Math.floor(numar1));
        System.out.println("Math.ceil(): " + Math.ceil(numar1));
        System.out.println("Math.round(): " + Math.round(numar1));

        System.out.println("Numărul: " + numar2);
        System.out.println("Math.floor(): " + Math.floor(numar2));
        System.out.println("Math.ceil(): " + Math.ceil(numar2));
        System.out.println("Math.round(): " + Math.round(numar2));

        System.out.println("Numărul: " + numar3);
        System.out.println("Math.floor(): " + Math.floor(numar3));
        System.out.println("Math.ceil(): " + Math.ceil(numar3));
        System.out.println("Math.round(): " + Math.round(numar3));
    }
}

//math.floor realizeaza rotunjire in jos
//math.ceil realizeaza rotunjire in sus
//math.round realizeaza rotunjire la valoarea intreaga cea mai apropiata